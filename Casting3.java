package casting;

public class Casting3 {
    public static void main(String[] args) {
        long maxIntValue = 2222222;
        long maxIntOver = 22222222222L; //long형은 L붙임
        int intOver;

        intOver = (int) maxIntValue;
        System.out.println(maxIntValue);
        System.out.println(maxIntOver);
        System.out.println(IntOver);
    }
}
